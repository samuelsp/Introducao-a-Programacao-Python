##Formataçoes em Python
# \n -> insere uma nova linha
print("Algum texto aqui\ne esse outro texto em outra linha.")

# \t -> insere um espaço entre palavras
print("Eu posso inserir um\tespaço entre palavras para formatar meu texto.")

# + -> concatena strings
print('Esse texto' + ' eu junto com esse outro texto.')
print("Esse texto'" + "' eu junto novamente com esse outro texto.")
print('Esse texto em aspas simples"' + " esse em aspas duplas.'")

# \ -> caractere de escape
print("Eu posso inserir uma palavra que contem \"aspas\" em meu texto.")
print('Eu posso inserir uma palavra que contem \'aspas\' em meu texto.')
